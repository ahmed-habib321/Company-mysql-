    private void customButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customButton9ActionPerformed
        String search = "select * from EmployeeData where ";
        if (rad1.isSelected()) {
            search += "Number ";
        } else if (rad2.isSelected()) {
            search += "Name ";
        } else if (rad3.isSelected()) {
            search += "Address ";
        } else if (rad4.isSelected()) {
            search += "Salary ";
        } else if (rad5.isSelected()) {
            search += "BirthDate ";
        } else if (rad6.isSelected()) {
            search += "HiringDate ";
        } else if (rad7.isSelected()) {
            search += "Department ";
        } else if (rad8.isSelected()) {
            search += "DepartmentNumber ";
        } else {
            String s = p.getEmployeeNumberByPhone(Searchbar.getText());
            search += "Number like '%" + s + "%'";
        }
        if (rad1.isSelected() || rad2.isSelected() || rad3.isSelected() || rad4.isSelected() || rad5.isSelected() || rad6.isSelected() || rad7.isSelected() || rad8.isSelected()) {
            search += " like '%" + Searchbar.getText() + "%'";
        }
        e.filter(search, DateTable);
    }//GEN-LAST:event_customButton9ActionPerformed

