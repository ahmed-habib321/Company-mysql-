    private void setvalues(Employee emp) {
        emp.setEmployeeNumber(Integer.parseInt(Empnum.getText()));
        emp.setEmployeeName(Empnam.getText());
        emp.setEmployeeAddress(Empadd.getText());
        emp.setSalary(Double.parseDouble(Empsal.getText()));
        String HDate = f.format(Emphi.getDate());
        String BDate = f.format(Empbi.getDate());
        emp.setHireDate(HDate);
        emp.setBirthDate(BDate);
        String dn = dep.getValue(Dep.getSelectedItem().toString());
        emp.setDepartmentNumber(Integer.parseInt(dn));
        p.setEmployeeNumber(Integer.parseInt(Empnum.getText()));
    }

    private void cleardata() {
        Tools.clearTextFields(this);
        Empnum.setText(e.getAutoNumber());
        addb.setEnabled(true);
        updateb.setEnabled(false);
        delb.setEnabled(false);
        delp2.setEnabled(false);
        delp2ActionPerformed(null);
        Empnam.requestFocus();
    }

