    private void customButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customButton10ActionPerformed
        int num = Integer.parseInt(dep.getAutoNumber());
        String nam = (String) Tools.showInputDialog("Enter Department Name");
        if (nam != null) {
            String loc = (String) Tools.showInputDialog("Enter Department Location");
            if (loc != null) {
                dep.setDepartmentName(nam);
                dep.setDepartmentNumber(num);
                dep.setLocation(loc);
                dep.add();
            } else {
                Tools.showMessageDialog("the adding is canceled");
            }
        } else {
            Tools.showMessageDialog("the adding is canceled");
        }
        MysqlDatabaseManager.fillComboBox("Department", "Department", Dep);
    }//GEN-LAST:event_customButton10ActionPerformed

    private void DateTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DateTableMouseClicked
        try {
            int row = DateTable.getSelectedRow();
            String Depn = DateTable.getValueAt(row, 6).toString();
            Empnum.setText(DateTable.getValueAt(row, 0).toString());
            Empnam.setText(DateTable.getValueAt(row, 1).toString());
            Empadd.setText(DateTable.getValueAt(row, 2).toString());
            Empsal.setText(DateTable.getValueAt(row, 3).toString());
            Date hd = f.parse(DateTable.getValueAt(row, 4).toString());
            Date bd = f.parse(DateTable.getValueAt(row, 5).toString());
            Emphi.setDate(hd);
            Empbi.setDate(bd);
            Dep.setSelectedItem(DateTable.getValueAt(row, 7).toString());
            p.setEmployeeNumber(Integer.parseInt(Empnum.getText()));
            p.getAllRows(phones);
            addb.setEnabled(false);
            updateb.setEnabled(true);
            delb.setEnabled(true);
            delp2.setEnabled(true);
        } catch (Exception ex) {
            Tools.showMessageDialog(ex.getMessage());
        }

    }//GEN-LAST:event_DateTableMouseClicked

