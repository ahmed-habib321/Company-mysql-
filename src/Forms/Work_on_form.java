    private void setvalues(WorkOn wo) {
        String empname = emp.getSelectedItem().toString();
        String proname = pro.getSelectedItem().toString();
        int empno = Integer.parseInt(e.getValue(empname));
        int prono = Integer.parseInt(p.getValue(proname));
        wo.setEmployeeNumber(empno);
        wo.setProjectNumber(prono);
    }

    private void customButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customButton10ActionPerformed
        int num = Integer.parseInt(p.getAutoNumber());
        String nam = (String) Tools.showInputDialog("Enter Project Name");
        if (nam != null) {
            String loc = (String) Tools.showInputDialog("Enter project Location");
            if (loc != null) {
                String depn = (String) Tools.showInputDialog("Enter Department number");
                if (depn != null) {
                    p.setProjectName(nam);
                    p.setProjectNumber(num);
                    p.setLocation(loc);
                    p.setDepartmentNumber(Integer.parseInt(depn));
                    p.add();
                } else {
                    Tools.showMessageDialog("the adding is canceled");
                }
            } else {
                Tools.showMessageDialog("the adding is canceled");
            }
        } else {
            Tools.showMessageDialog("the adding is canceled");
        }
        MysqlDatabaseManager.fillComboBox("ProjectData", "Project", pro);
    }//GEN-LAST:event_customButton10ActionPerformed

