    private void delbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delbActionPerformed
        if (Tools.showConfirmationDialog("Are you sure you want to delete this Relation")) {
            setvalues(wo);
            setvalues(recwo);
            wo.delete();
            wo.getAllRows(DateTable);
        }
        delb.setEnabled(false);
        addb.setEnabled(true);
        recwork.setEnabled(true);
    }//GEN-LAST:event_delbActionPerformed

    private void customButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customButton4ActionPerformed
        String dnum = (String) Tools.showInputDialog("Enter Employee Number");
        if (dnum != null) {
            wo.setEmployeeNumber(Integer.parseInt(dnum));
            String Query = "select * from WorkOnData where EmployeeNumber = " + Integer.parseInt(dnum);
            wo.filter(Query, DateTable);
        }
    }//GEN-LAST:event_customButton4ActionPerformed

