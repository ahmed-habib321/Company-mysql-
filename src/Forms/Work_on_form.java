    private void customButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customButton9ActionPerformed
        String search = "select * from WorkOnData where ";
        if (rad1.isSelected()) {
            search += "EmployeeNumber ";
        } else if (rad2.isSelected()) {
            search += "EmployeeName ";
        } else if (rad3.isSelected()) {
            search += "ProjectNumber ";
        } else {
            search += "ProjectName ";
        }
        search += " like '%" + Searchbar.getText() + "%'";
        e.filter(search, DateTable);
    }//GEN-LAST:event_customButton9ActionPerformed

