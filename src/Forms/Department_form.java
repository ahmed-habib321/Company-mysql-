    private void DateTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DateTableMouseClicked
        try {
            int row = DateTable.getSelectedRow();
            String Dpnum = DateTable.getValueAt(row, 0).toString();
            String Dpnam = DateTable.getValueAt(row, 1).toString();
            String Dpdep = DateTable.getValueAt(row, 2).toString();

            dpnumber.setText(Dpnum);
            dpname.setText(Dpnam);
            dplocation.setText(Dpdep);

            addb.setEnabled(false);
            updateb.setEnabled(true);
            delb.setEnabled(true);
        } catch (Exception ex) {
            Tools.showMessageDialog(ex.getMessage());
        }
    }//GEN-LAST:event_DateTableMouseClicked

    private void recDepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_recDepActionPerformed
        if (Tools.showConfirmationDialog("do you want to recover Department (" + rec.getDepartmentName()+ " , " + rec.getLocation() + " )")) {

            int row = DateTable.getRowCount();
            for (int i = 0; i < row; i++) {
                int val = Integer.parseInt(DateTable.getValueAt(i, 0).toString());
                if (rec.getDepartmentNumber() == val) {
                    rec.setDepartmentNumber(Integer.parseInt(rec.getAutoNumber()));
                }
            }

            rec.add();
            d.getAllRows(DateTable);
            recDep.setEnabled(false);
        }
    }//GEN-LAST:event_recDepActionPerformed

    private void customButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customButton9ActionPerformed
        String search = "select * from DepartmentData where ";
        if (rad2.isSelected()) {
            search += "DepartmentNumber ";
        } else if (rad1.isSelected()) {
            search += "Department ";
        } else {
            search += "Location ";
        }
        search += " like '%" + Searchbar.getText() + "%'";
        d.filter(search, DateTable);
    }//GEN-LAST:event_customButton9ActionPerformed

