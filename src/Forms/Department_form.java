    private void setvalues() {
        d.setDepartmentName(dpname.getText());
        d.setDepartmentNumber(Integer.parseInt(dpnumber.getText()));
        d.setLocation(dplocation.getText());
    }

    private void setrec(int num, String name, String loc) {
        rec.setDepartmentName(name);
        rec.setDepartmentNumber(num);
        rec.setLocation(loc);
    }

    private void cleardata() {
        Tools.clearTextFields(this);
        dpnumber.setText(d.getAutoNumber());
        addb.setEnabled(true);
        updateb.setEnabled(false);
        delb.setEnabled(false);
        dpname.requestFocus();
    }

    private void delbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delbActionPerformed
        if (Tools.showConfirmationDialog("Are you sure you want to delete this Department " + dpnumber.getText())) {
            setvalues();
            setrec(d.getDepartmentNumber(), d.getDepartmentName(), d.getLocation());
            d.delete();
            d.getAllRows(DateTable);
            cleardata();
            recDep.setEnabled(true);
        }
    }//GEN-LAST:event_delbActionPerformed

